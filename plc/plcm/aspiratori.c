#include "all.inc"

void aspiratori(void)
{
    if (CONF_ASPIRATORI) 
    {
        // RESET ALLARMI ASPIRATORE FUMI 
        if (RESET_ALL || RESET_MC)  {
            TERM_ASP_FAL = 0;
        }

        /**** Termico Aspiratore Fumi scattato ****/
        if (!TERM_ASP_OK_I && ALIM_PRES_I)
            TERM_ASP_FAL = 1;

        // COMANDO ASPIRATORE FUMI : č ritentivo
        MANASP = MANO && (FUNZ_MAN84 && COMANDO_B || MANASP) && !(FUNZ_MAN84 && COMANDO_A) && !TERM_ASP_FAL && !RN_STARTASP;

        // Avvio aspiratore fumi 
        RIP_ASPIRATORE_O = ASPIRATORE_O = ((RN_STARTASP || MANASP) && !TERM_ASP_FAL);

        // LED Funzioni Manuali
        ASP_FUNES_A = MANO && FUNZ_MAN84 && !ASPIRATORE_O;
        ASP_FUNES_B = MANO && FUNZ_MAN84 && ASPIRATORE_O;
    }
    else
    {
        TERM_ASP_FAL = 0;

    }   
}
